%%VALORES DE X
x=[100	1000	5000	10000	50000	100000	125000	175000	200000	225000	275000	300000	325000	375000	400000	425000	500000
];
 
%%VALORES DE Y = F(x)
y=[1.1587E-04	1.1416E-02	2.9315E-01	1.1458E+00	2.8529E+01	1.1473E+02	1.7813E+02	3.4923E+02	4.5963E+02	5.8432E+02	8.8232E+02	1.0521E+03	1.2263E+03	1.6229E+03	1.8575E+03	2.2145E+03	3.0193E+03
];

%%OBTENER UN POLINOMIO QUE SE AJUSTE A LOS PUNTOS X Y Y
n=2; 				%%GRADO DEL POLINOMIO DE AJUSTE
p=polyfit(x,y,n)	%%FUNCION QUE REALIZA EL AJUSTE POLINOMIAL

%%SE CREA UN ESPACIO PARA REALIZAR UNA GRAFICA EN 
%%DONDE SE COMPARA LA CURVA AJUSTADA CONTRA LOS 
%%VALORES EXPERIMENTALES
xi=linspace(0,500000,10000);	%%ESPACIO DE PUNTOS PARA Xi
z=polyval(p,xi);				%%EVALUACION DEL POLINOMIO P EN EL ESPACIO Xi

%%SE REALIZA LA FIGURA CORRESPONDIENTE
figure(1);
plot(x,y,'Bo-',xi,z,'-'),grid;
ylabel('Segundos');
xlabel('Numeros a ordenar');
title('Ajuste de minimos cuadrados Burbuja');